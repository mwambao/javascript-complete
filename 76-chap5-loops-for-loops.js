/*
    for (initialize variable; condition; statement) {
    //code block to be executed
    }
*/

for (let i=0 ; i <10; i++) {
    console.log(i)
}


/*
Explanation:
* Initialization: let i = 0; - This initializes the variable i to 0 before the loop starts.

* Condition: i < 10; - This checks the condition before each iteration of the loop. The loop will continue as long as i is less than 10.

* Increment: i++ - This increments the value of i by 1 after each iteration of the loop.

* Body: console.log(i); - This prints the current value of i to the console.
*/