//random number generator we will use Math.random() function in javascript.

console.log(Math.random()); //prints a random number between 0 and 1

console.log(Math.floor(Math.random() * 10)); //prints a random number between 0 and 9

console.log(Math.ceil(Math.random() * 10)); //prints a random number between 1 and 10

console.log(Math.round(Math.random() * 10)); //prints a random number between 0 and 10