
//PARAMETER - A parameter is defined as a variable listed inside the parentheses of the function definiction, which defines the scope of the function.
function tester(parameter1, parameter2) {
    return parameter1 + " " + parameter2;
}



//ARGUMENT - When a function is called we simply add the arguments to it.
console.log(tester("argument1", "argument2")); // argument1 argument2