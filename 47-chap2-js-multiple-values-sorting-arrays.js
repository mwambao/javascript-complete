/*
sort() -> sorts elements A-Z and numbers from lowers to highest(ascending order)
*/

let ourNames = ['James', 'Alicia', 'Faith', 'Maria', 'Bert'];
ourNames.sort();

console.log(ourNames); //[ 'Alicia', 'Bert', 'Faith', 'James', 'Maria' 

let myNums = [18, 72, 33, 56, 40];
myNums.sort();

console.log(myNums); //[ 18, 33, 40, 56, 72 ]
